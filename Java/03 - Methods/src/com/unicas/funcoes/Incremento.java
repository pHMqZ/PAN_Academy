package com.unicas.funcoes;

public interface Incremento {
	  public static void main(String[] args) {
		   
		  
		  int x = 5, y = 5;

		  System.out.println(++x); // imprime 6 
		  System.out.println(x); // imprime 6

		  System.out.println(y++); // imprime 5 
		  System.out.println(y); // imprime 6
	  }
	}

